fun main(){

    var day="Monday"


    when(day){


        "Mon"-> println("Monday")
        "Tue"-> println("Tuesday")
        "Wed"-> println("Wednesday")
        "Thurs"-> println("Thursday")
        "Fri"-> println("Friday")
        "Sat"-> println("Saturday")
        "Sun"-> println("Sunday")


      else-> {
          println("Not a day")
      }
    }

}